"""
Offline GNN training script.

Trains the PhysicsGNN on trajectory data generated by ``gnn/generate_data.py``.

Usage
-----
    python gnn/train_gnn.py --config configs/real.yaml \\
        --use_gnn_world_model true --gnn_train true \\
        --gnn_data_dir data/gnn/ --gnn_checkpoint_dir checkpoints/gnn/

Training details:
    * Adam optimiser, lr=1e-4, cosine annealing schedule, 100 epochs
    * Batch by trajectory, unroll 10 frames (truncated BPTT)
    * Loss: MSE on velocity delta predictions vs Warp ground truth
    * Gaussian noise injection (σ=0.003 m) on positions/velocities
    * Best checkpoint saved by validation loss
    * Optional WandB logging (uses existing project config)
"""

